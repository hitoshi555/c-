#include <bits/stdc++.h>
using namespace std;

int main()
{
    int A, B;
    cin >> A >> B;
    // ここにプログラムを追記
    cout << "A:";
    int n = 0;
    while (n < A)
    {
        cout << "]";
        n++;
    }
    cout << endl;

    cout << "B:";
    int i = 0;
    while (i < B)
    {
        cout << "]";
        i++;
    }
    cout << endl;
}